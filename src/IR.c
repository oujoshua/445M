// default producer
void IR_Producer(unsigned short data) {
  if(OS_Fifo_Put(data) == 0){ // send to consumer
    IR_DataLost++;
  }   
}

// default consumer
void IR_Consumer(void) {
  while(1) {
    unsigned long distance = IR_Distance(OS_Fifo_Get());
    if(IR_BuffIdx < IR_BUFFLEN) {
      IR_Buff[IR_BuffIdx++] = distance;
    }
  }
}

// dump buffer data to UART
void IR_Dump(void) {
  int i;
  printf("Conents of the IR sensor's buffer:\n");
  for(i = 0; i < IR_BuffIdx; i++) {
    printf("%d cm\n", IR_Buff[i]);
  }
}

// reset IR buffer
void IR_Reset(void) {
  IR_BuffIdx = 0;
}

// thread for monitoring all 4 ADC channels
unsigned short raw_samples[CHANNELS];
long IR_dist_cm[CHANNELS];
void IR_MasterThread(void) {
  int i;
  while(1) {
    ADC_Mailbox_Receive(raw_samples);
    // convert each sample to centimeters
    for(i = 0; i < CHANNELS; i++) {
      IR_dist_cm[i] = IR_Distance(raw_samples[i]);
    }
  }
}
